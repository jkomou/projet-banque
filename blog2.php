            <h2>Les causes de l’hépatite</h2>
            <br>
            <p>On parle d’hépatite pour désigner une inflammation du foie. Elle est provoquée par une réaction du système de défense immunitaire face aux agressions que subissent les cellules. En outre, l’hépatite est une maladie qui peut être expliquée par une infection d’ordre virale ou non.</p>
            <p>L’hépatite peut être virale. Dans ce cas, elle est expliquée par la présence d’un virus de type A, B, C, D et E. Cette classification alphabétique permet simplement de faire une distinction entre les différents types d’hépatites. En effet, les virus sont tous différents : ils ont chacun leurs modes de transmission, leur formes évolutives et leurs traitements.</p>
            <br>
            <h2>Les differents types d'hépatites</h2>
            <br>
            <p>Il existe donc 5 virus à l’origine de 5 formes hépatiques distinctes. Elles peuvent être regroupées dans 3 catégories en fonction de leurs modes de transmission.</p>
            <ul>
                <li><h4>Les hépatites A et E</h4></li>
                    <p>La transmission de ces hépatites se fait par voie alimentaire ou par ingestion d’eau contaminée par des excréments, des déjections animales ou des produits chimiques. Les conditions d’hygiène et de sécurité alimentaire et sanitaire sont donc capitales dans l’apparition de la maladie</p>
                    <br>
                    <br>
                <li><h4>Les hépatites B et D</h4></li>
                    <p>L’hépatite D ne peut pas être contractée seule. En effet, le virus de type D ne se transmet que chez les personnes infectées par l’hépatite B. La transmission est simultanée ou postérieure à l’infection par le virus de type B.
                    La transmission du virus  se fait par voie sanguine ou sexuelle. L’infection est rendue possible par l’intermédiaire de liquides sexuels (sperme, sécrétions vaginales) ou de contacts avec du sang infecté (plaies ouvertes, échange de seringues).</p>
                    </p>
